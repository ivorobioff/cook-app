package cloud.familythings.cook.model.filter;


public class DishFilter {
    private String name;
    private String notes;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getNotes() {
        return notes;
    }
}
